/* ── UTF-8 decode ──────────────────────────────────────────────────── */

/* Strict-enough decoder shared by the draw AND measure paths (single source of
 * truth, or centered/right-aligned layouts drift). Rejects surrogates and
 * overlong forms; any invalid byte consumes exactly 1 byte and yields U+FFFD
 * so a bad sequence can never stall a render loop. */
int dm_utf8_decode(const char *s, uint32_t *cp) {
    const unsigned char *b = (const unsigned char *)s;
    if (b[0] < 0x80) { *cp = b[0]; return b[0] ? 1 : 0; }
    if ((b[0] & 0xE0) == 0xC0 && (b[1] & 0xC0) == 0x80) {
        uint32_t c = ((uint32_t)(b[0] & 0x1F) << 6) | (uint32_t)(b[1] & 0x3F);
        if (c >= 0x80) { *cp = c; return 2; }
    } else if ((b[0] & 0xF0) == 0xE0 && (b[1] & 0xC0) == 0x80 &&
               (b[2] & 0xC0) == 0x80) {
        uint32_t c = ((uint32_t)(b[0] & 0x0F) << 12) |
                     ((uint32_t)(b[1] & 0x3F) << 6) | (uint32_t)(b[2] & 0x3F);
        if (c >= 0x800 && (c < 0xD800 || c > 0xDFFF)) { *cp = c; return 3; }
    } else if ((b[0] & 0xF8) == 0xF0 && (b[1] & 0xC0) == 0x80 &&
               (b[2] & 0xC0) == 0x80 && (b[3] & 0xC0) == 0x80) {
        uint32_t c = ((uint32_t)(b[0] & 0x07) << 18) |
                     ((uint32_t)(b[1] & 0x3F) << 12) |
                     ((uint32_t)(b[2] & 0x3F) << 6) | (uint32_t)(b[3] & 0x3F);
        if (c >= 0x10000 && c <= 0x10FFFF) { *cp = c; return 4; }
    }
    *cp = 0xFFFD;
    return 1;
}

int dm_utf8_len(const char *s) {
    int n = 0;
    uint32_t cp;
    int adv;
    while ((adv = dm_utf8_decode(s, &cp)) > 0) { n++; s += adv; }
    return n;
}

/* ── Extended glyph blob (.dmf, built by tools/genfont.py from Unifont) ──
 * Layout (little-endian, BMP only): 16-byte header ("DMF1", ver, glyph_h,
 * flags, glyph count, heap size), u32 top[256] file offsets of page tables
 * (0 = absent), page tables of u32[256] (0xFFFFFFFF = absent, else
 * bit0 = fullwidth, bits31..1 = heap byte offset), then the bitmap heap.
 * The heap starts at file_size - heap_size; entries are bounds-checked per
 * lookup so a truncated/corrupt blob degrades to tofu, never OOB reads. */

#include <stdio.h>
#include <stdlib.h>

static struct {
    const uint8_t *data;   /* whole blob, malloc'd */
    size_t size;
    size_t heap_base;      /* file offset of the bitmap heap */
    size_t heap_size;
    int state;             /* 0 = untried, 1 = loaded, -1 = unavailable */
} g_ext;

static uint32_t ext_rd32(size_t off) {
    const uint8_t *p = g_ext.data + off;
    return (uint32_t)p[0] | ((uint32_t)p[1] << 8) |
           ((uint32_t)p[2] << 16) | ((uint32_t)p[3] << 24);
}

int dm_font_load_ext(const char *path) {
    FILE *fp = fopen(path, "rb");
    if (!fp) return -1;
    fseek(fp, 0, SEEK_END);
    long sz = ftell(fp);
    fseek(fp, 0, SEEK_SET);
    if (sz < 16 + 256 * 4) { fclose(fp); return -1; }
    uint8_t *buf = malloc((size_t)sz);
    if (!buf || fread(buf, 1, (size_t)sz, fp) != (size_t)sz) {
        free(buf);
        fclose(fp);
        return -1;
    }
    fclose(fp);
    if (memcmp(buf, "DMF1", 4) != 0 || buf[4] != 1 || buf[5] != 16) {
        free(buf);
        return -1;
    }
    uint32_t heap_size = (uint32_t)buf[12] | ((uint32_t)buf[13] << 8) |
                         ((uint32_t)buf[14] << 16) | ((uint32_t)buf[15] << 24);
    if (heap_size > (size_t)sz - (16 + 256 * 4)) {
        free(buf);
        return -1;
    }
    if (g_ext.data) free((void *)g_ext.data);
    g_ext.data = buf;
    g_ext.size = (size_t)sz;
    g_ext.heap_size = heap_size;
    g_ext.heap_base = (size_t)sz - heap_size;
    g_ext.state = 1;
    return 0;
}

static void ext_autoload(void) {
    if (g_ext.state != 0) return;
    g_ext.state = -1; /* one shot: stay unavailable unless a load succeeds */
    const char *env = getenv("DEMOD_FONT");
    if (env && *env && dm_font_load_ext(env) == 0) return;
    const char *home = getenv("HOME");
    if (home && *home) {
        char p[512];
        snprintf(p, sizeof(p), "%s/.local/share/demod/unifont.dmf", home);
        if (dm_font_load_ext(p) == 0) return;
    }
    (void)dm_font_load_ext("unifont.dmf");
}

/* Look up a non-ASCII glyph. Returns bitmap rows (16 or 32 bytes) or NULL. */
static const uint8_t *ext_glyph(uint32_t cp, int *fullwidth) {
    if (cp > 0xFFFF) return NULL; /* v1 blob is BMP-only */
    if (g_ext.state == 0) ext_autoload();
    if (g_ext.state < 0) return NULL;
    uint32_t page = ext_rd32(16 + (size_t)(cp >> 8) * 4);
    if (page == 0 || page + 256 * 4 > g_ext.heap_base) return NULL;
    uint32_t entry = ext_rd32(page + (size_t)(cp & 0xFF) * 4);
    if (entry == 0xFFFFFFFFu) return NULL;
    int fw = (int)(entry & 1u);
    size_t off = (size_t)(entry >> 1);
    size_t bytes = fw ? 32 : 16;
    if (off + bytes > g_ext.heap_size) return NULL;
    *fullwidth = fw;
    return g_ext.data + g_ext.heap_base + off;
}

/* ── Measurement ───────────────────────────────────────────────────── */

int dm_font_cp_advance(const DmFont *f, uint32_t cp) {
    if (cp < 0x80) return f->glyph_w; /* ASCII fast path, blob untouched */
    int fw = 0;
    return (ext_glyph(cp, &fw) && fw) ? f->glyph_w * 2 : f->glyph_w;
}

int dm_font_text_width_n(const DmFont *f, const char *text, int nbytes) {
    int w = 0;
    const char *end = (nbytes < 0) ? NULL : text + nbytes;
    uint32_t cp;
    int adv;
    while ((!end || text < end) && (adv = dm_utf8_decode(text, &cp)) > 0) {
        w += dm_font_cp_advance(f, cp);
        text += adv;
    }
    return w;
}

int dm_font_text_width(const DmFont *f, const char *text) {
    return dm_font_text_width_n(f, text, -1);
}

int dm_font_text_height(const DmFont *f) {
    return f->glyph_h;
}

int dm_font_text_width_scaled(const DmFont *f, const char *text, int scale) {
    if (scale < 1) scale = 1;
    return dm_font_text_width(f, text) * scale;
}

/* ── Drawing ───────────────────────────────────────────────────────── */

/* Draw one codepoint at integer scale; returns its advance in pixels (scaled).
 * ASCII comes from the compiled-in table; everything else from the .dmf blob.
 * Missing glyphs draw a hollow "tofu" box (never blank — blank cells read as
 * layout bugs) with a halfwidth advance. */
static int draw_cp_scaled(DmFramebuffer *fb, const DmFont *f,
                          int x, int y, uint32_t cp, DmColor fg, int scale) {
    const uint8_t *glyph = NULL;
    int fw = 0;

    if (cp < 0x80) {
        int idx = (int)cp - f->first_cp;
        if (idx >= 0 && idx < f->num_glyphs)
            glyph = f->data + (size_t)idx * f->glyph_h;
        else
            return f->glyph_w * scale; /* control chars: advance, draw nothing */
    } else {
        glyph = ext_glyph(cp, &fw);
    }

    if (!glyph) { /* tofu */
        int w = f->glyph_w * scale, h = f->glyph_h * scale;
        DmRect r = { x + scale, y + 2 * scale, w - 2 * scale, h - 5 * scale };
        dm_fb_fill_rect(fb, (DmRect){ r.x, r.y, r.w, scale }, fg);
        dm_fb_fill_rect(fb, (DmRect){ r.x, r.y + r.h - scale, r.w, scale }, fg);
        dm_fb_fill_rect(fb, (DmRect){ r.x, r.y, scale, r.h }, fg);
        dm_fb_fill_rect(fb, (DmRect){ r.x + r.w - scale, r.y, scale, r.h }, fg);
        return f->glyph_w * scale;
    }

    int bpr = fw ? 2 : 1; /* bytes per row */
    for (int row = 0; row < f->glyph_h; row++) {
        for (int b = 0; b < bpr; b++) {
            uint8_t bits = glyph[row * bpr + b];
            if (!bits) continue;
            int bx = x + b * 8 * scale;
            for (int col = 0; col < 8; col++) {
                if (bits & (0x80 >> col)) {
                    if (scale == 1)
                        dm_fb_put_pixel(fb, bx + col, y + row, fg);
                    else
                        dm_fb_fill_rect(fb, (DmRect){ bx + col * scale,
                                                      y + row * scale,
                                                      scale, scale }, fg);
                }
            }
        }
    }
    return f->glyph_w * (fw ? 2 : 1) * scale;
}

void dm_fb_draw_char(DmFramebuffer *fb, const DmFont *f,
                     int x, int y, char ch, DmColor fg) {
    draw_cp_scaled(fb, f, x, y, (uint32_t)(unsigned char)ch, fg, 1);
}

void dm_fb_draw_text_scaled(DmFramebuffer *fb, const DmFont *f,
                            int x, int y, const char *text, DmColor fg, int scale) {
    if (scale < 1) scale = 1;
    uint32_t cp;
    int adv;
    while ((adv = dm_utf8_decode(text, &cp)) > 0) {
        x += draw_cp_scaled(fb, f, x, y, cp, fg, scale);
        text += adv;
    }
}

void dm_fb_draw_text(DmFramebuffer *fb, const DmFont *f,
                     int x, int y, const char *text, DmColor fg) {
    dm_fb_draw_text_scaled(fb, f, x, y, text, fg, 1);
}

void dm_fb_draw_text_centered(DmFramebuffer *fb, const DmFont *f,
                              DmRect bounds, const char *text, DmColor fg) {
    int tw = dm_font_text_width(f, text);
    int th = dm_font_text_height(f);
    int x = bounds.x + (bounds.w - tw) / 2;
    int y = bounds.y + (bounds.h - th) / 2;
    dm_fb_draw_text(fb, f, x, y, text, fg);
}
